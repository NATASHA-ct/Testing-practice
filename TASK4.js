class Calculate {

        //add method
        static add(a,b){
            return a + b;
        }
    
        //multiply method
        static multiply(a, b){
            return a * b;
        }
       
        // substract method
        static substract(a, b){
            return a - b;
        }

        // divide method
        static divide(a, b){
            return a / b;
        }
    }

    module.exports = Calculate;